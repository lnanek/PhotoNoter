package co.spark.jaja;

class Circle {
	private float radius;
	private int x;
	private int y;

	public float getRadius() {

		return radius;
	}

	public void setRadius(float radius) {
		this.radius = radius;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}